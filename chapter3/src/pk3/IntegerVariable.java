package pk3;

public class IntegerVariable {

	public static void main(String[] args) {
		
		short sVal=10;
		byte bVal=20;
		
		int result= sVal+bVal;
		System.out.println("두수의 합은 : " + (sVal+bVal)); //() 표시가 있어야 계산한다
		System.out.println("두수의 합은 : " + result);
		//섞어서 계산 가능
		

	}

}
